<h2>Le Wattway</h2>





		<div>
<p>Colas, filiale du groupe Bouygues et leader mondial de la construction de routes a imaginé un concept de revêtement routier photovoltaïque unique au monde. Cette prouesse technique donne une nouvelle fonction à la route : celle de produire de l’énergie propre et renouve­lable. Celle-ci, protégée par deux brevets, introduit une rupture majeure en conférant à la route une nouvelle fonction de production locale d’énergie propre et renouve­lable, en complément de son usage traditionnel de support de la mobilité. Ainsi, Wattway permettra d’alimenter l’éclairage public, les bureaux, les enseignes lumineuses, les tramways, mais aussi les logements, etc... Pour alimenter un foyer en électricité (hors chauffage), 20 m² de chaussée Wattway suffisent. Et grâce à un kilomètre linéaire de route équipée de dalles Wattway, il est possible d’éclairer une ville de 5 000 habitants.</p>
<br><br>
<img src="images/wattway.jpg" width="300px" alt="WATTWAY" class="pictureG" />

Depuis de nombreuses années, le concept de « routes électriques » est en train de voir le jour, c'est-à-dire la route, mais aussi des ponts et des bâtiments qui, pendant des années, redonneront aux hommes l'énergie qu'ils ont dépensée pour les construire et qui pourront à l'avenir produire plus qu'ils dépensent. Fini les coulées d'asphalte à l'infini, mais les éoliennes et les panneaux solaires situés le long des routes sont ce que souhaitent vivement les concepteurs et les personnes soucieuses de l'environnement de tous les coins du monde. Mais peut-être qu’ils n’attendront pas trop longtemps : grâce à Wattway, ce concept pourrait devenir réalité.
<br><br>
En effet, grâce aux campagnes françaises, sur les 1000 km et plus de routes françaises, des panneaux photovoltaïques spéciaux, réalisés par l'Institut national de l'énergie solaire (INES) et par Colas, une société spécialisée dans les infrastructures de transport, seront installés cela a nécessité 5 ans de recherche. Le nom de cette nouvelle technologie s'appelle Wattway et consiste en des panneaux de 7 mm d'épaisseur et de 15 mm de large dans lesquels reposent, dans un substrat, des cellules photovoltaïques en silicium polycristallin communes capables de transformer l'énergie solaire en électricité. Mais la particularité de Wattway ne consiste pas à disposer de cellules photovoltaïques, mais bien à la superposition qui les protège à la fois par les agents atmosphériques et, principalement, par les voitures et les camions tirant des poids sur les routes.
<br><br>
De plus, les cellules sont immergées dans plusieurs couches d’une résine spéciale qui assure le contrôle de la route, évite l’usure des pneus et est conforme à la dilatation thermique, garantissant la sécurité et durabilité. De plus, ces panneaux résistent à la pluie et développent un indice de protection de IP66. Ils ont facilement passé les tests de chasse-neige. Wattway, l'asphalte photovoltaïque qui produit de l'énergie.



