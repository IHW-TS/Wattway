<p> Depuis l'avènement de l'ère industrielle au milieu du XIXe siècle, l'humanité ne cesse d'accroître sa consommation 
d'énergie sous des formes multiples pour répondre à la complexification des sociétés (industrie, transports, chauffage,
 électricité, etc...). Aujourd’hui, le monde est à la recherche de nouvelles façons de produire de l’énergie. 
Nous utilisons les énergies fossiles traditionnelles comme le gaz, le charbon ou encore le pétrole qui s’épuisent,
 or nous savons maintenant que la combustion de ceux-ci « nous détraque le climat » ainsi que l’épuisement de celle-ci. 
Le nucléaire, bien qu’étant une énergie plus propre du point de vue carbone, est de plus en plus remis en cause par l’opinion
 publique à cause des craintes qu’il suscite (notamment après l’incident de Fukushima). Alors on pourrait se demander si il y a d'autres
 sources d'énergie, d'autres voies possibles ? Face à ces évolutions, les énergies renouvelables se posent comme une alternative inévitable.
 On cherche à développer les énergies éoliennes, les énergies marines et houlomotrices, la biomasse, ou encore le solaire.
Parmi les dernières innovations suscitant l’intérêt dans ce domaine, on peut notamment citer les routes solaires.</p>
