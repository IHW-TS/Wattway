<h2>Contexte historique</h2>





		<div>
<p>Réchauffement climatique, amenuisement des ressources primaires, augmentation de la demande énergétique… Ces enjeux remettent
 en cause notre manière de consommer, ainsi que de réfléchir à un moyen de produire l’électricité de demain.
</p><br>
Le projet est né d'une initiative de Colas en 2005 visant à <b>élargir l'utilisation des routes</b>. Selon un communiqué de presse,
 l'ingénieur Jean-Luc Gautier s'est rendu compte que <b>les routes passent 90% de leur temps à simplement regarder le ciel. Quand le soleil brille, elles sont bien sûr exposées à ses rayons.
 C'est une surface idéale pour l'énergie applications</b>.
Celui-ci a commencé à expérimenter dans son garage en 2010 avant que l'entreprise ne se lance dans 
le projet pour trouver un moyen de fabriquer un panneau solaire fragile 
suffisamment durable pour être utilisé. Grâce à un <b>substrat multicouche composé de résines et de polymères</b>, la société explique que
 ses panneaux solaires à haut rendement <b>peuvent être utilisés sur tous les types d'infrastructures routières (routes, autoroutes, parkings, pistes cyclables, etc.)</b>.
<br><br><img src="images/pphoto3.jpg" alt="WATTWAY" width="532" height="282" class="pictureR" />
On note que le <b>"concept solaire n'est pas nouveau"</b>, soulignant la piste cyclable SolaRoad de 230 pieds aux Pays-Bas qui existe depuis novembre 2014 et
 <b>produit déjà plus d'énergie que prévu</b>. En outre, ils mentionnent le projet Solar Roadways basé en Idaho, qui est actuellement financé par le financement participatif.
Néanmoins, ces efforts sont à peine comparables à l’ampleur de l’initiative française qui,
 si elle aboutissait, pourrait éventuellement être déployée dans le monde entier. Colas espère également que cela <b>ouvrira la voie à des routes intelligentes</b> qui amélioreraient la gestion du trafic, 
rechargerait les véhicules électriques, éliminerait le givre et hébergerait des voitures autonomes.
<br>
C’est ainsi que le premier revêtement routier photovoltaïque au monde est née : 
le Wattway. Créées pour nos besoins en transport, les routes pourraient remplir une nouvelle fonction : produire de l'électricité.
 C’est une innovation française brevetée au terme de 5 années de recherche menées par Colas, et l’INES, Institut National de l’énergie Solaire.
<br><br>
C’est pourquoi Colas, a présenté une solution de revêtement solaire photovoltaïque qui se posent sur la route existante, 
sans besoin de la reconstruire. De plus en associant les techniques de la construction routière avec celles de la 
production photovoltaïque, Wattway fournit une énergie propre et renouvelable, tout en permettant la circulation de tout type de véhicule. La première route solaire est née.
WATTWAY. Ce leader mondial de la construction de routes, a annoncé le 13 octobre 2015 la commercialisation de route solaire à partir du 1er janvier 2016.


