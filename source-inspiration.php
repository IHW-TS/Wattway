	  <p>Le projet Serpent Solaire a été inventé par le Suédois <strong>Mans Tham</strong>, architecte et stratège urbain basé à Stockholm. 
Ce projet a été pour la première fois présenté lors de la conférence <strong>« Toward a Just Metropolis » de l&rsquo;UC Berkeley (University of California)</strong> le 18 juin 2010. 
L&rsquo;idée est de recouvrir les routes d&rsquo;ombrières solaires photovoltaïques. </p><br>
<img src="images/solar.jpg" width="350px" alt="solar" class="pictureG" />
	    Cette expérience a été mise en place pour la première fois en Belgique. Ce tunnel solaire belge, a une longueur de 2,1 km de la ligne de chemin de fer Paris-Amsterdam, 
devrait générer annuellement 3 300 MWh d&rsquo;énergie propre. Ceci pour un coût d&rsquo;investissement de 15 M€ et une puissance installée de 4 MW, soit de 3,8 €/W. Le même projet,
 compte tenu de la chute vertigineuse des coûts du solaire PV entre 2010 et 2015, coûterait aujourd&rsquo;hui entre 2 et 2,5€/W.
   
 <br><br>  L'infrastructure transparente permet aux automobilistes de continuer à contempler les paysages traversés, mais les protège également des rayons du soleil. 
Ce qui inciterait donc les conducteurs à limiter leur usage de la climatisation et donc à réduire à la fois leur consommation de carburant et les émissions de particules polluantes.
<br><br>
	  Cette autoroute pourrait donner un effet maximum de 115 MW. Ce serait assez d'énergie <strong>&ldquo;150 gigawattheures&rdquo;</strong> pour couvrir les besoins en électricité de tous
 les foyers de Venice, en Californie. Production locale pour la consommation locale dans le réseau avec des pertes de transmission minimales.

      <br><br>La route est libre de ses deux côtés pour admirer le paysage confortablement, car seul le ciel est remplacé. Il n&rsquo;y a aucun risque de claustrophobie, le ciel laisse 
passer la lumière du jour et donc on n&rsquo;a pas l'impression d&rsquo;être dans un tunnel.

<br><br><img src="images/solar2.jpg" width="350px" alt="solar" class="pictureR" />
	   En juin 2011, un an après la révélation du concept de Mans Tham, d'autres pays, installent ce système au-dessus de parkings et de rivières.
 Pour les rivières, cela diminue l'évaporation de l'eau ce qui diminue à son tour l'assèchement des rivières lors de grandes sécheresses. Ces cellules photovoltaïques ont un 
rendement optimal, car elles ne subissent pas d&rsquo;ombrages et moins de salissures ce qui constitue un énorme avantage comparativement aux routes solaires où les cellules PV 
sont placées au sol et donc où l&rsquo;ombrage et les salissures sont présentes. Il est possible d&rsquo;orienter ses écailles vis-à-vis du soleil pour qu&rsquo;elle bénéficie d'un
 ensoleillement optimal et d&rsquo;un refroidissement aérien pour un meilleur rendement. Comme les cellules ne sont pas écrasées, elles durent plus longtemps et coûtent moins cher au final. 
L&rsquo;intérêt écologique est donc bien plus important. <br>
      <br><br>
	 Le serpent solaire empêche l&rsquo;eau de pluie de ruisseler sur les routes. Grâce à celui-ci, non seulement l&rsquo;eau n&rsquo;est plus polluée, 
mais en plus, elle peut être collectée, stockée, et ainsi être utile pour les activités agricoles du voisinage. Avec en outre une contribution à la réduction des risques d&rsquo;inondation.
 On peut aussi remarquer quelques autres avantages : Dans l'intérêt écologique on remarque une réduction d&rsquo;activité minière et industrielle polluantes car moins de consommation de matière
 première par unité d&rsquo;électricité produite.
