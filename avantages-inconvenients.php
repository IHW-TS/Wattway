          <p>Bien sûr, les routes composées de panneaux solaires semblent être une excellente idée et pourraient permettre à notre pays de fonctionner réellement à l'énergie solaire. Cependant, à quel point ce plan est-il réalisable ?
            <br><br><img src="images/pphoto2.jpg" alt="WATTWAY" width="532" height="332" class="pictureR" />
            D'une part, ce serait très coûteux. La construction de chaque panneau coûte environ 1800 euros, et le plan prévoit que des milliards d’entre eux couvriront les routes. L'installation prendrait énormément de temps et d'argent, de même que la formation des équipes pour les entretenir correctement. Très probablement, il faudrait plusieurs années avant que l'électricité générée par les panneaux ne récupère ses propres coûts.
  <br>
            Il y a ensuite la question de la durabilité. Nos routes sont très punies par les voitures, les camions, les motos et les semi-remorques, sans oublier le fait qu'ils pourraient être endommagés lors d'accidents de la route. Comment ces panneaux de verre résisteraient-ils à ce type de punition ? Et si nous dépendons des cellules solaires pour les feux de circulation et de la puissance des voitures électriques, que se passera-t-il si les capteurs de lumière solaire sont endommagés ? En outre, le coût de la réparation de ces panneaux solaires est probablement plus élevé que pour la réparation de routes asphaltées ordinaires. La société a déclaré qu’elle pourrait utiliser un type de verre autonettoyant pour protéger la surface des saletées et des salissures, mais ce processus n’a pas encore fait ses preuves.
  <br><br>
            De plus, il y a le gros problème de l'énergie solaire: les jours nuageux. La technologie actuelle de l'énergie solaire est très inefficace. En Fait, la plupart des panneaux solaires ne convertissent qu'environ 14% de l'énergie disponible en électricité. Et les jours où la lumière du soleil n’est pas facilement accessible, comme lors des longs hivers de nombreuses régions du pays, il faut se demander d’où proviendrait l’énergie. C’est la raison pour laquelle l’énergie solaire n’est considérée que comme un type de source d’énergie renouvelable plutôt qu’une unique source d’énergie. Il est donc difficile de compter sur elle.
  <br>
            Malgré ces inconvénients, il ne fait aucun doute que les autoroutes à panneaux solaires sont une idée unique et révolutionnaire. C’est le genre de réflexion que nous devons faire pour nous sortir de notre dépendance aux énergies fossiles et pour utiliser une source d’énergie plus écologique. Donc, dans quelques années, vous risquez de vous retrouver sur des panneaux solaires en verre au lieu d’asphalte.
 
