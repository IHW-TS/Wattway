          <p>Le principal avantage des routes solaires est qu'elles génèrent des énergies renouvelables. Les promoteurs envisagent ainsi de réduire leur consommation de combustibles fossiles et de fournir aux villes une source d’énergie renouvelable. L'électricité générée par les systèmes solaires photovoltaïques est respectueuse de l'environnement et n'émet aucune contamination. Cela pourrait réduire la dépendance vis-à-vis des sources d’énergie classiques telles que le charbon, le pétrole et d’autres combustibles fossiles. En outre, la durée de vie des panneaux solaires est d’environ 20 ans, ce qui est bien supérieur aux routes goudronnées classiques, qui ne durent que 7 à 12 ans. C'est bénéfique à la fois pour l'environnement et pour le gouvernement, c'est pourquoi tant de pays encouragent leurs citoyens à les installer.
            L’un des grands avantage du concept de la chaussée solaire est qu’il utilise une source d’énergie renouvelable pour produire de l’électricité. <br><br>
            Colas nous propose plusieurs avantages sur sa route solaire comme le passage à la fois des Véhicule légers et des poids lourds ainsi qu’une utilisation d'une surface déjà utilisée pour les transport : un atout par rapport aux centrales solaires classiques qui occupe de grandes surfaces au sol. De plus, la raison pour laquel Wattway est si révolutionnaire, c’est aussi grâce à sa technique de pose. Le Wattway se compose principalement de dalles qui s’installent directement sur les chaussées existantes, sans travaux de génie civil (contrairement au système américain Solar Roadways). Il est l’idéal pour l’électrification des zones isolées du réseau électrique. Wattway est accompagné d’un système éprouvé et disponible sur le marché
  <br><br>
            Mais, bien sûr, le point fort de Wattway est l’économie d’énergie. En moyenne, une route est occupée par des voitures pendant 10% du temps, alors que le reste du temps, elle fait face au soleil et accumule potentiellement de grandes quantités d'énergie, principalement en été. 20 m² de ces panneaux sont capables de produire assez d'électricité pour alimenter une seule habitation, 15 m² pourraient suffire pour alimenter un feu de signalisation, alors qu'une route longue de 1 km pourrait alimenter une petite ville de 5 000 habitants. Selon Hervé le Boucchinì, directeur général de Colas, si ces panneaux étaient installés sur 1 000 km de routes françaises, ils couvriraient 10% des besoins nationaux.
  <br>
            En plus de fournir de l'électricité verte, les systèmes solaires routiers seront dotés de nombreuses fonctionnalités de haute technologie. Cela inclura la capacité de chauffer, ce qui peut faire fondre la glace et la neige, en veillant à ce que votre voiture ne soit pas bloquée et que vos chemins ne soient pas glissants après des chutes de neige abondantes ou par temps froid.
