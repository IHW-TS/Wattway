Les composants de cette route solaire sont 100 % français. Wattway est une dalle photovoltaïque de 1,40 m de long par 0,70 m de 
large et de 7 mm d’épaisseur. C'est un substrat composé de couches dans lesquelles 28 cellules photovoltaïques en Polycristallin / mono-like qui
 sont disposées en quinconce et encapsulées ( dans un complexe de différents matériaux qui permettent de les protéger mécaniquement et de rendre 
